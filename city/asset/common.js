export function checkDiff(str1, str2) {
  if (!str1 || !str2) return;
  let a = str1
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[ \-\_\(\)\/\\\[\]\{\}',.;:!><=\+]/g, "")
    .toLowerCase();
  let b = str2
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[ \-\_\(\)\/\\\[\]\{\}',.;:!><=\+]/g, "")
    .toLowerCase();
  const m = a.length,
    n = b.length;
  const dp = Array.from({ length: m + 1 }, () => Array(n + 1).fill(0));

  for (let i = 0; i <= m; i++) dp[i][0] = i;
  for (let j = 0; j <= n; j++) dp[0][j] = j;

  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      const cost = a[i - 1] === b[j - 1] ? 0 : 1;
      dp[i][j] = Math.min(
        dp[i - 1][j] + 1,
        dp[i][j - 1] + 1,
        dp[i - 1][j - 1] + cost
      );
    }
  }
  return dp[m][n];
}